/*

Difference Between OOP And POP


POP ==>  POP stands for Procedure Oriented Programming.
		 Large program divided into Functions.
		 Follow Top - Down Approach.
		 No Data Hiding concept available.
		 Code Reusability ==> NO
		 Printf() and Scanf() functions are use for receive and print data from user.
		 
OOP ==>  OOP stands for Object Oriented Programming.
		 Large program divided into Class and Object.
		 Follow Bottom - Up Approach.
		 Data Hiding concept available in C++.
		 Code Reusability using Inheritance.
		 cout<< and cin>> functions are use for receive and print data from user.
		 
		 
*/